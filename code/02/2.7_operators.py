# 2.7_operators.py
print(2 + 3)  # 5
print(3 - 2)  # 1
print(2 * 3)  # 6
print(3 / 2)  # 1.5

# Exponents / 지수
print(3 ** 2)  # 9
print(3 ** 3)  # 27
print(10 ** 6)  # 1000000

# Remainders / 나머지
print(4 % 2)  # 0
print(5 % 2)  # 1
print(6 % 2)  # 0
print(7 % 2)  # 1

# Floor division / 정수 나눗셈
print(4 // 2)  # 2
print(5 // 2)  # 2
print(6 // 2)  # 3
print(7 // 2)  # 3

# Order of operations / 연산 순서
print(2 + 3 * 4)  # 14
print((2 + 3) * 4)  # 20
