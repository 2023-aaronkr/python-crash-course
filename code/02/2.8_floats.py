# 2.8_floats.py
print(0.1 + 0.1)  # 0.2
print(0.2 + 0.2)  # 0.4
print(2 * 0.1)  # 0.2
print(2 * 0.2)  # 0.4

# Imprecise calculations / 부정확한 계산
print(0.2 + 0.1)  # 0.30000000000000004
print(3 * 0.1)  # 0.30000000000000004

# Division always results in floats / 나눗셈은 항상 부동 소수점 수가 됩니다
print(4 / 2)  # 2.0
print(5 / 2)  # 2.5
print(6 / 2)  # 3.0
print(7 / 2)  # 3.5
