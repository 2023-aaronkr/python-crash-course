# 4.1_list_loops.py
magicians = ['alice', 'david', 'carolina']
for magician in magicians:
    print(magician)

# Choose a meaningful name / 요소의 이름을 지정해준다
cats = ['bengal', 'persian', 'russian blue']
for cat in cats:
    print(cat)

dogs = ['bulldog', 'poodle', 'labrador']
for dog in dogs:
    print(dog)

items = ['pen', 'pencil', 'eraser']
for item in items:
    print(item)
